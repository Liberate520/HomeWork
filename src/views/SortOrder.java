package views;

/**
 * Перечисление SortOrder определяет порядок сортировки генеалогического древа.
 */
public enum SortOrder {
    /**
     * Сортировка по имени.
     */
    NAME,
    /**
     * Сортировка по дате рождения.
     */
    BIRTH_DATE,
    /**
     * Без сортировки (в исходном порядке).
     */
    UNSORTED,
    /**
     * Отображение загруженных данных.
     */
    LOADED
}
